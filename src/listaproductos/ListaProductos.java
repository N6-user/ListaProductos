    private DefaultTableModel modelo = new DefaultTableModel();

    private void armarCabecera() {
        modelo.addColumn("Nombre");
        modelo.addColumn("Precio");
        modelo.addColumn("Categoría");
        jtProductos.setModel(modelo);
    }

    private void armarComboBox() {
        jcbCategoría.addItem("Electrónica");
        jcbCategoría.addItem("Ropa");
        jcbCategoría.addItem("Alimentos");
        jcbCategoría.addItem("Limpieza");
        jcbCategoría.addItem("Mueblería");
    }

    private void agregarProducto(String nombre, double precio, String categoria) {
        modelo.addRow(new Object[]{nombre, precio, categoria});
    }

