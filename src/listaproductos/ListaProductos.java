    private void jbAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarActionPerformed
        try {
            double precio = Double.parseDouble(jtfPrecio.getText());
            if (precio < 0) {
                JOptionPane.showMessageDialog(this, "Debe ingresar un número real positivo en el campo \"Precio\".");
                return;
            }
            String categoria = (String) jcbCategoría.getSelectedItem();
            String nombre = (String) jtfNombre.getText();
            if (nombre.equals("")) {
                JOptionPane.showMessageDialog(this, "Debe ingresar un nombre.");
                return;
            }
            agregarProducto(nombre, precio, categoria);
            jtfNombre.setText("");
            jtfPrecio.setText("");
        } catch (NumberFormatException nfEx) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un número real positivo en el campo \"Precio\".");
        }
    }//GEN-LAST:event_jbAgregarActionPerformed

